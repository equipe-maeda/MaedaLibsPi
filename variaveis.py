# Variável, em linguagem de programação, é uma região na memória do computador onde se armazena informação de forma binária

# Python é uma linguagem de tipagem dinâmica.

nome = "Renato Oliveira"
idade = 48
peso = 86.3

tipo_formato = '{} é um {}' # A chave é um marcador de formatação

# Obs.: A função print() é  uma função da biblioteca pyton que imprime no terminal do computador. 

print(tipo_formato.format(nome, type(nome)))
print(tipo_formato.format(idade, type(idade)))
print(tipo_formato.format(peso, type(peso)))


